#include "libft.h"

size_t		ft_strlen(const char *str)
{
	char	*temp;
	int		i;

	i = 0;
	temp = (char *)str;
	while (*temp != '\0')
	{
		i++;
		temp = temp + 1;
	}
	return (i);
}
